export const getCardsByCategories = (categories: CategoryId[]): Card[] =>
  CARDS.filter((card) => categories.includes(card.category))

/** Number of cards per category. */
export const getCardCountByCategory = (): Record<CategoryId, number> => {
  const ids: CategoryId[] = ['anatomy', 'dental', 'diseases', 'tools', 'facts', 'professions']
  return Object.fromEntries(
    ids.map((id) => [id, CARDS.filter((c) => c.category === id).length]),
  ) as Record<CategoryId, number>
}

export const pickRandomCards = (cards: Card[], count: number): Card[] => {
  const pool = [...cards]
  const picked: Card[] = []
  const max = Math.min(count, pool.length)
  for (let i = 0; i < max; i += 1) {
    const index = Math.floor(Math.random() * pool.length)
    const [card] = pool.splice(index, 1)
    if (card) picked.push(card)
  }
  return picked
}

const excludeIdSet = (excludeIds: string[]) => new Set(excludeIds)

/** Pick up to `count` random cards from `cards` that are not in `excludeIds`. No repeats. */
export function pickRandomCardsExcluding(
  cards: Card[],
  count: number,
  excludeIds: string[],
): Card[] {
  const exclude = excludeIdSet(excludeIds)
  const pool = cards.filter((c) => !exclude.has(c.id))
  return pickRandomCards(pool, count)
}

/** Check if there are enough cards in the given categories for the requested count. */
export function hasEnoughCards(categories: CategoryId[], count: number): boolean {
  const available = getCardsByCategories(categories)
  return available.length >= count
}

// simple helper to get full Card from id using current categories map
export const getCardById = (id: string): Card | undefined =>
  CARDS.find((card) => card.id === id)

